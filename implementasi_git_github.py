# Implementasi Operasional Git dan GitHub menggunakan Python

print("=== Simulasi Operasional Git dan GitHub ===")

print("\nLangkah-langkah penggunaan Git:")
print("1. git init        → Membuat repository lokal di komputer")
print("2. git add .       → Menambahkan semua file ke staging area")
print("3. git commit -m 'pesan' → Menyimpan perubahan dengan pesan tertentu")
print("4. git branch -M main → Menentukan branch utama bernama main")
print("5. git remote add origin [URL GitHub] → Menghubungkan ke repository GitHub")
print("6. git push -u origin main → Mengirim perubahan ke GitHub (upload)")

print("\nKeterangan:")
print("- Git digunakan untuk mengelola versi dan perubahan kode secara lokal.")
print("- GitHub digunakan untuk menyimpan repository Git secara online agar bisa diakses dan dikerjakan bersama.")

print("\nSimulasi selesai. Semua langkah Git dan GitHub berhasil dijelaskan.")
